# 成績表のデータ 
p_list = [
    ["田中", 30, 50, 80],
    ["井上", 80, 20, 40],
    ["鈴木", 80, 30, 40],
    ["斉藤", 70, 78, 76]
]
# 科目のラベル --- (*1)
sub_list = ["国語", "算数", "社会"]

# 科目の数だけ繰り返す --- (*2)
for i in range(3):
    k = i + 1 # リストの何番目か
    # 合計点を調べる --- (*3)
    total = 0
    for p in p_list:
        v = p[k] # 該当する点を得る --- (*4)
        total = total + v
    # 平均点を計算 --- (*5)
    ave = total / len(p_list)
    print(sub_list[i] + "=" + str(ave))

