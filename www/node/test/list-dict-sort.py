# あるクラスのテスト結果
test_list = [
  { "名前": "田中", "国語": 80, "算数": 45, "社会": 90 },
  { "名前": "鈴川", "国語": 62, "算数": 70, "社会": 58 },
  { "名前": "早川", "国語": 77, "算数": 69, "社会": 74 }
]

# 各生徒について合計点を求める --- (*1)
for p in test_list:
    p["合計"] = p["国語"] + p["算数"] + p["社会"]

# 合計点で生徒を並び替える --- (*2)
test_list = sorted(test_list,
        key=lambda v: v["合計"], 
        reverse=True)

# 結果を表示
for p in test_list:
    print(p["名前"] + ":" + str(p["合計"]))

