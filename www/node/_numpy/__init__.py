import numpy as n

def count_nonzero(c):
    return n.count_nonzero(c)
def array(*a,**k):
    return n.array(*a,**k)
def zeros(*a,**k):
    return n.zeros(*a,**k)
def sum(*a,**k):
    return n.sum(*a,**k)
def argsort(*a,**k):
    return n.argsort(*a,**k)
def arange(*a,**k):
    return n.arange(*a,**k)
def sin(*a,**k):
    return n.sin(*a,**k)
def cos(*a,**k):
    return n.cos(*a,**k)
def linspace(*a,**k):
    return n.linspace(*a,**k)
